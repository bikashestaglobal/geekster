package com.counter.counter.model;

public class Counter {
    private int count;
    private String name;

    public int getCount() {
        return count;
    }

    public int setCount(int count) {
        this.count = count;
        return count;
    }

    public String getName() {
        return name;
    }

    public String setName(String name) {
        this.name = name;
        return name;
    }
}
